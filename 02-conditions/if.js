// Conditional Statements: if, else, else if

// Problem 1: Check if a number is positive or negative
let number = 10;

if (number > 0) {
  console.log("The number is positive");
} else if (number < 0) {
  console.log("The number is negative");
} else {
  console.log("The number is zero");
}

// Problem 2: Check if a number is even or odd
let num = 7;

if (num % 2 === 0) {
  console.log("Even number");
} else {
  console.log("Odd number");
}

// Problem 3: Find the largest of two numbers
let a = 15;
let b = 20;

if (a > b) {
  console.log("a is larger");
} else {
  console.log("b is larger");
}

// Problem 4: Check pass or fail
let marks = 65;

if (marks >= 40) {
  console.log("Pass");
} else {
  console.log("Fail");
}

// Problem 5: Check driving eligibility
let age = 18;

if (age >= 18) {
  console.log("Eligible for driving");
} else {
  console.log("Not eligible for driving");
}
